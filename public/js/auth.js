import axios from 'axios';
import { showAlert } from './alerts';

export const login = async (email, password) => {
  try {
    const res = await axios({
      method: 'POST',
      url: 'http://127.0.0.1:3000/api/v1/users/login',
      data: {
        email,
        password
      }
    });

    if (res.data.status === 'success') {
      showAlert('success', 'Successfully Logged In');

      window.setTimeout(() => {
        location.assign('/');
      }, 1500);
    }
  } catch (err) {
    showAlert('error', err.response.data.message);
  }
};

export const logout = async () => {
  try {
    const res = await axios({
      method: 'GET',
      url: 'http://127.0.0.1:3000/api/v1/users/logout'
    });

    if ((res.data.status = 'success')) {
      // location.reload(true);
      window.setTimeout(() => {
        location.assign('/login');
      }, 1000);
      showAlert('success', 'logged out succesfully');
    }
  } catch (err) {
    console.log(err.response);
    showAlert('error', 'Error logging out! Try again.');
  }
};

export const signup = async (name, email, password, passConfirmed) => {
  try {
    const res = await axios({
      method: 'POST',
      url: 'http://127.0.0.1:3000/api/v1/users/signup',
      data: {
        name,
        email,
        password,
        passConfirmed
      }
    });
    console.log(res);
    if (res.data.status === 'success') {
      showAlert('success', 'Signed Up Succesfully');
      window.setTimeout(() => {
        location.assign('/me');
      }, 1500);
    }
  } catch (error) {
    console.log(error.response.data.message);
    if (
      error.response.data.message ===
      'User validation failed: passConfirmed: Passwords are not the same!'
    ) {
      showAlert('error', 'Passwords are not the same');
    } else showAlert('error', error.response.data.message);
  }
};
